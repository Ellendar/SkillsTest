package com.englandlogistics.scm.skills_test.third;

/**
 * Integer triple for use with SumOfThree
 * @author jgill
 */
public class Triple {
	public int x, y, z;
	
	public Triple() {
		
	}
	
	public Triple(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public int sum() {
		return x + y + z;
	}
}